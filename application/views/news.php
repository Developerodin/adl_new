                        </h5>

                        <div class="col-md-6 ">
                            <div class="mt-2">

                                <p><span style="font-weight: 700;">• Scope: </span>Bookkeeping focuses on recording
                                    financial transactions, while accounting involves interpreting and analysing
                                    financial data to provide insights and support decision-making.</p>
                            </div>
                        </div>
                        <div class="col-md-6 mt-3">
                            <div class="mt-2">

                                <p><span style="font-weight: 700;">• Level of Analysis:</span> Bookkeeping deals with
                                    transactional details, whereas accounting involves higher-level analysis and
                                    interpretation of financial information.</p>
                            </div>
                        </div>
                        <div class="col-md-6 mt-3">
                            <div class="mt-2">

                                <p><span style="font-weight: 700;">• Timing: </span> Bookkeeping records transactions as
                                    they occur, while accounting typically involves periodic reporting and analysis,
                                    such as quarterly or annually.</p>
                            </div>
                        </div>
                        <div class="col-md-6 mt-3">
                            <div class="mt-2">

                                <p><span style="font-weight: 700;">• Skills Required: </span>Bookkeeping requires strong
                                    attention to detail and proficiency in record-keeping, while accounting necessitates
                                    analytical skills, financial acumen and knowledge of accounting principles and
                                    standards.</p>
                            </div>
                        </div>
