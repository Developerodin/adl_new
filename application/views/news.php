                        <div class="col-md-6">
                            <div class="mt-2">

                                <p><span style="font-weight: 700;">• Financial analysis: </span> Accountants analyse
                                    financial data to assess a company's profitability, liquidity, and solvency,
                                    identifying trends, patterns and areas for improvement.</p>
                            </div>
                        </div>
                        <div class="col-md-6 mt-3">
                            <div class="mt-2">

                                <p><span style="font-weight: 700;">• Budgeting and forecasting: </span> They help in
                                    developing budgets and financial forecasts based on historical data and future
                                    projections, aiding in strategic planning and resource allocation.</p>
                            </div>
                        </div>
                        <div class="col-md-6 mt-3">
                            <div class="mt-2">

                                <p><span style="font-weight: 700;">• Tax planning and compliance: </span> Accountants
                                    ensure compliance with tax regulations, prepare tax returns and advise on tax
                                    planning strategies to minimize tax liabilities while maximizing savings.</p>
                            </div>
                        </div>
