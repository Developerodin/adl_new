                            <div class="mt-2">

                                <p><span style="font-weight: 700;">• Financial reporting: </span> Accountants prepare
                                    and interpret financial statements, providing stakeholders with accurate and timely
                                    information about the company's financial performance.</p>
                            </div>
                        </div>
                    </div>
                    <div class="col-md-12 row mt-5">
